//@ts-ignore
function reduceLinks(data: SankeyLink[][]): SankeyLinkExtended[] {
    return ([] as SankeyLink[]).concat
        .apply([], data) // flat
        .reduce<SankeyLinkExtended[]>((accumulated, current) => {
            const existing = accumulated.find((item) => item.source === current.source && item.target === current.target && item.valueid === current.valueid && item.paperName === current.paperName);
            if (!existing) {
                //@ts-ignore
                accumulated.push({
                    source: current.source,
                    target: current.target,
                    value: current.value,
                    valueid: current.valueid,
                    paperName: current.paperName,
                    process: current.process,
                    id: current.id,
                    imgUrl: current.imgUrl,
                });
            } else {
                existing.value += 1;
            }
            return accumulated;
        }, []);
}

